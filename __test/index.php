        $(".contact-form-input").on('change', function(){
	        var att = $(this).attr('name');
	        var val = $(this).val();
	        
           	$.get( 
            	"_php/mail.php",
				{ 
					a: att, 
					v: val 
				},
				function(data) {
                $('#form_repsonse').html(data);
              }
           );
		});
        
        $(".contact-form-button").click(function(){
	        var nam = $('#form_name').val();
	        var tel = $('#form_tel').val();
	        var eml = $('#form_email').val();
	        var msg = $('#form_message').val();
	        
           	$.get( 
            	"_php/mail.php",
				{ 
					a: "send", 
					n: nam,
					t: tel,
					e: eml,
					m: msg 
				},
				function(data) {
					var arr = data.split('||');
					if(arr[0] == "e"){
						$('#form_repsonse').html(arr[1]);
					} else {
						$('#contact_form').get(0).reset();
						$('#form_finish').toggle();
						$('#form_finish').html(arr[1]);
						setTimeout( function(){ 
							$('#form_finish').fadeOut();
  						}  , 1000 );					
					}
	            }
           );
		});
	        
        var hashValue = window.location.hash;
        if(hashValue == "" || hashValue == "#landing"){
	        document.getElementById('menu').style.display = "none";
        }	       
	        
        $(window).on("scroll", function(){
	        
	        var hashValue = window.location.hash;
	        
	        if(hashValue == "#landing"){		        
		        $('#menu').fadeOut(500);		        
	        } else {		        
		        $('#menu').fadeIn(1000); 
		        
		        var bodyClass = $("body").attr("class");       
				var bodyArr = bodyClass.split("-");
				
				var hrefAttrls = $(".menu-btns").map(function(){return $(this).attr("id");}).get();
				
				$.each(hrefAttrls, function(index, value){
					var  hrefId = "menu-btn-"+bodyArr[2];
					if(hrefId == value){
						$("#menu-btn-"+bodyArr[2]).addclass("menu-btn-on");				
					} else {
						$("#menu-btn-"+bodyArr[2]).removeClass("menu-btn-on");						
					}				
				});
	        }
        
        });
